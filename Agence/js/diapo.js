// ----------------------------BAKER_GUENNOUN------------------------
	$(function(){
		$("#gallerie_BAKER_GUENNOUN ul li:last-child").prependTo("#gallerie_BAKER_GUENNOUN ul");
				
		$("#Bgauche_BAKER_GUENNOUN").click(function(){
			gauche();
		});
		
		$("#Bdroite_BAKER_GUENNOUN").click(function(){
			droite();
		});
		
		function gauche()
		{
			$("#gallerie_BAKER_GUENNOUN ul").animate({left: "300px"},200,function(){
				$("#gallerie_BAKER_GUENNOUN ul li:last-child").prependTo("#gallerie_BAKER_GUENNOUN ul");
				$("#gallerie_BAKER_GUENNOUN ul").css("left","");
			});
		}
		
		function droite()
		{	
			$("#gallerie_BAKER_GUENNOUN ul").animate({right: "300px"},200,function(){
				$("#gallerie_BAKER_GUENNOUN ul li:first-child").appendTo("#gallerie_BAKER_GUENNOUN ul");
				$("#gallerie_BAKER_GUENNOUN ul").css("right","");
			});
		}
	});

